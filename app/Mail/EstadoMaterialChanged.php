     *
     * @param array $data
     */
    public function __construct($data)
    {
        $this->data = $data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this->from('diegoalexisayala27@gmail.com') // Corrige la direcciÃ³n de correo aquÃ­
                    ->subject('Cambio de estado de material')
                    ->view('emails.estadoMaterialChanged')
